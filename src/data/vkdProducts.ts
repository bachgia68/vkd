export const findVKDProduct = (slug: string): VKDProduct | undefined =>
  vkdProducts.find((p) => p.slug === slug);

export const formatVND = (price: number): string =>
  new Intl.NumberFormat('vi-VN').format(price) + '₫';

// Quy đổi tham khảo — cùng tỉ giá đã dùng khi khởi tạo dữ liệu sản phẩm (25.500 VND/USD)
const VND_PER_USD = 25500;

export interface CartCompatibleProduct {
  id: string;
  name: string;
  nameVi: string;
  category: string;
  healthGoal: 'energy' | 'stress' | 'immunity' | 'youth';
  audiences: ('men' | 'women' | 'seniors' | 'executives')[];
  priceUSD: number;
  priceVND: number;
  priceJPY: number;
  priceCNY: number;
  priceEUR: number;
  activeIngredient: string;
  description: string;
  descriptionVi: string;
  image: string;
  badge: string;
  rating: number;
  reviews: number;
}

const healthGoalByCategory: Record<CategoryId, CartCompatibleProduct['healthGoal']> = {
  ginseng: 'immunity',
  supplements: 'energy',
  tea_wine: 'stress',
  cosmetics: 'youth',
};

export function toCartProduct(p: VKDProduct): CartCompatibleProduct {
  const priceUSD = Math.round((p.price / VND_PER_USD) * 100) / 100;
  return {
    id: p.sku,
    name: p.name,
    nameVi: p.name,
    category: p.category,
    healthGoal: healthGoalByCategory[p.category],
    audiences: ['men', 'women', 'seniors', 'executives'],
    priceUSD,
    priceVND: p.price,
    priceJPY: Math.round(priceUSD * 150),
    priceCNY: Math.round(priceUSD * 7.2 * 100) / 100,
    priceEUR: Math.round(priceUSD * 0.93 * 100) / 100,
    activeIngredient: p.activeIngredient,
    description: p.description,
    descriptionVi: p.description,
    image: p.image,
    badge: p.badge ?? '',
    rating: p.rating,
    reviews: p.reviews,
  };
}
